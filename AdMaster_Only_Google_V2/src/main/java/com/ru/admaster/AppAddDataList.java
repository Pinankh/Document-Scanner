public class AppAddDataList {

    String package_name;
    String app_link;
    String native_banner;
    String native_icon;
    String native_title;
    String native_desc;

    public String getPackage_name() {
        return package_name;
    }

    public void setPackage_name(String package_name) {
        this.package_name = package_name;
    }

    public String getApp_link() {
        return app_link;
    }

    public void setApp_link(String app_link) {
        this.app_link = app_link;
    }

    public String getNative_banner() {
        return native_banner;
    }

    public void setNative_banner(String native_banner) {
        this.native_banner = native_banner;
    }

    public String getNative_icon() {
        return native_icon;
    }

    public void setNative_icon(String native_icon) {
        this.native_icon = native_icon;
    }

    public String getNative_title() {
        return native_title;
    }

    public void setNative_title(String native_title) {
        this.native_title = native_title;
    }

    public String getNative_desc() {
        return native_desc;
    }

    public void setNative_desc(String native_desc) {
        this.native_desc = native_desc;
    }
}
